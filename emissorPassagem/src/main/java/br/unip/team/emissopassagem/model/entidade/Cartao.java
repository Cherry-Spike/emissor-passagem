package br.unip.team.emissopassagem.model.entidade;

public class Cartao {
	private String numero;
	private int pin;
	
	public Cartao() {
		
	}

	public String getNumero() {
		return numero;
	}

	public void setNumero(String numero) {
		this.numero = numero;
	}

	public int getPin() {
		return pin;
	}

	public void setPin(int pin) {
		this.pin = pin;
	}		

}
